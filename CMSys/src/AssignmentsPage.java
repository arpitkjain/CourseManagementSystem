    private void AssignmentLogMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AssignmentLogMouseClicked
        // TODO add your handling code here:
        
        int row=AssignmentLog.getSelectedRow();
        int col=AssignmentLog.getSelectedColumn();
        if(col==0){
            String s = (String) AssignmentLog.getValueAt(row,0);
            if(personType==0) {
                JOptionPane.showMessageDialog(AssignmentLog, s);
            }
            else {
                JOptionPane.showMessageDialog(AssignmentLog, course.viewSubmission(row));       
            }
        } else if (personType==1){
            String grd = JOptionPane.showInputDialog("Enter grade:");
            course.assignments.get(row).grade = grd;
            AssignmentLog.setValueAt(grd, row, col);
        }
    }//GEN-LAST:event_AssignmentLogMouseClicked

