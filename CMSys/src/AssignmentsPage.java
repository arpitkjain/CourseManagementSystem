    public void showAssignments() {
        DefaultTableModel mo=(DefaultTableModel)AssignmentLog.getModel();
        if (mo.getRowCount() > 0) {
              for (int i = mo.getRowCount() - 1; i > -1; i--) {
                 mo.removeRow(i);
             }
        }
        for(int i=0; i<course.assignments.size(); i++){
            mo.addRow(new Object[]{course.assignments.get(i).problemStatement,
                course.assignments.get(i).grade});
        }
    }
