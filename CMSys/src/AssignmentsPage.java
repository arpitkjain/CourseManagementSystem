    //Submit or Create Assignment
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(personType==0){
            int row=AssignmentLog.getSelectedRow();
            if(row==-1) {
                JOptionPane.showMessageDialog(AssignmentLog, "Select an assignment!");
                return;
            }
            String submission= JOptionPane.showInputDialog("Write submission: ");
            course.submitAssignment(submission, row);
        }
        else {
            String asgn= JOptionPane.showInputDialog("Write assignment: ");
            course.createAssignment(asgn);
            DefaultTableModel mo=(DefaultTableModel)AssignmentLog.getModel();
            mo.addRow(new Object[]{course.assignments.get(course.assignments.size()-1).problemStatement,
                course.assignments.get(course.assignments.size()-1).grade});
        }
    }//GEN-LAST:event_jButton4ActionPerformed

