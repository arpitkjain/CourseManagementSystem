    public void showReplies() {
        DefaultTableModel mo=(DefaultTableModel)Replies.getModel();
        if (mo.getRowCount() > 0) {
              for (int i = mo.getRowCount() - 1; i > -1; i--) {
                 mo.removeRow(i);
             }
        }
        for(String reply : course.forum){
            mo.addRow(new Object[]{reply});
        }
    }
