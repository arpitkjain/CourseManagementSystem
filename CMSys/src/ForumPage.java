    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(Replies,"Empty reply!");
        }
        else {
                String user;
            if(personType==0)
                user = "Student: ";
            else if(personType==1)
                user = "Teacher: ";
            else {
                System.out.println("Wrong personType: "+personType);
                return;
            }
            DefaultTableModel mo=(DefaultTableModel)Replies.getModel();
            mo.addRow(new Object[]{user + jTextArea1.getText()});
            course.forum.add(user+jTextArea1.getText());
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

